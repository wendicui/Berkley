public class Count10 {
    public static void main(String[] args) {
        {
            int i, sum = "what happens";
            for ( i = 0; i <= 10 ; i++) {
                sum = sum + i;
            }
            System.out.println("the sum of" +
                                "1 through 10 is " +
                                sum);
        }
    }
}
